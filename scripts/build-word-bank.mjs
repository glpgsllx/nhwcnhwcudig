function readThuo(path) {
  return fs.readFileSync(path, "utf8")
    .split(/\r?\n/)
    .map((line) => line.trim().split(/\s+/)[0])
    .filter(Boolean);
}

function take(words, count, filter) {
  return unique(words.filter(filter)).slice(0, count);
}

function unique(words) {
  return [...new Set(words.map((word) => word.trim()).filter(Boolean))];
}

function isChineseWord(word) {
  return /^[\u4e00-\u9fa5]+$/.test(word);
}

function hasBadPart(word) {
  return /(有限公司|集团|公司|研究|中心|协会|学院|大学|网站|论坛|病毒|疾病|养殖|保护|系统|标准|配件|材料|技术|工程|传感器|控制|查询|有限公司|网$)/.test(word);
}

function foodFilter(word) {
  if (!isChineseWord(word) || word.length < 2 || word.length > 5 || hasBadPart(word)) return false;
  if (/(聚|硫|磷|氯|丙|乙|钠|钾|镁|铝|酯|酶|添加剂|防腐剂|稳定剂|漂白剂|着色剂|增稠剂|乳化剂|甜味剂|酸钙|酸钠|酸钾|酸镁|亚油酸|硬脂酸|苹果酸)/.test(word)) return false;
  if (/(茅台|五粮液|娃哈哈|康师傅|百事|健力宝|全聚德|轩尼诗|古井贡|剑南春|泸州老窖)/.test(word)) return false;
  if (/(金屋藏娇|苦中作乐|掌上明珠|大富豪|霸王别姬|金玉满堂|双喜临门|大丰收|龙凤呈祥|孔雀开屏|黄袍加身)/.test(word)) return false;
  if (/(硬脂酸|亚麻酸|苹果酸|柠檬酸|叶酸|烟酸|吐温|隆鑫|果后|肉眼|铁板|大肉|寸金|明胶|滑石粉|稳定剂)/.test(word)) return false;
  return true;
}

